// To perform Operator Precendence Example 6
class Program13
{
    public static void main(String args[]) 
    {
        int a=10;
        int b=-10;
        boolean c=true;
        boolean d=false;
        System.out.println(~a); // prints -11
        System.out.println(~b); // prints 9
        System.out.println(!c); // prints false
        System.out.println(!d); // prinrts true        
    }
}