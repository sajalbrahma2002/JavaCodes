// To perform Operator Precedence on Temperature Conversion from Fahrenheit to Celsius
class Program14
{
    public static void main(String args[])
    {
       System.out.println("Evaluating Temperature Conversion :- ");
       double fahrenheit=98.4;
       double celsius= (5.0*(fahrenheit - 32.0)/9.0);
       System.out.println(fahrenheit+" F is same as "+celsius+" C");   
    }
}